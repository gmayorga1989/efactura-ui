@Injectable({ providedIn: 'root' })
export class UiI18nService {
  private readonly http = inject(HttpClient);
  private readonly session = inject(SessionContextService);
  private readonly _language = signal<UiLanguage>(this.readLanguage());
  private initialProfileLanguageLoaded = false;
  readonly language = this._language.asReadonly();
  readonly dictionary = computed(() => TRANSLATIONS[this._language()]);

  setLanguage(language: string | null | undefined): void {
    const lang = this.normalize(language);
    this._language.set(lang);
    sessionStorage.setItem(STORAGE_KEY, lang);
  }

  initializeFromProfileOnce(): void {
    if (this.initialProfileLanguageLoaded) {
      return;
    }
    this.initialProfileLanguageLoaded = true;
    this.http.get<{ idioma?: string | null; avatarUrl?: string | null; enLinea?: boolean | null; ultimoPing?: string | null }>('/api/web/v1/me/perfil').subscribe({
      next: (perfil) => {
        if (perfil.idioma) {
          this.setLanguage(perfil.idioma);
        }
        const current = this.session.profile();
        if (current) {
          this.session.setMe({
            ...current,
            avatarUrl: perfil.avatarUrl ?? current.avatarUrl ?? null,
            enLinea: perfil.enLinea ?? current.enLinea ?? null,
            ultimoPing: perfil.ultimoPing ?? current.ultimoPing ?? null,
          });
        }
      },
      error: () => undefined,
    });
  }

  t(
    key: string,
    fallbackOrParams?: string | Record<string, unknown>,
    paramsOrFallback?: Record<string, unknown> | string,
  ): string {
    const fallback = typeof fallbackOrParams === 'string'
      ? fallbackOrParams
      : typeof paramsOrFallback === 'string'
        ? paramsOrFallback
        : undefined;
    const interpolation = typeof fallbackOrParams === 'object'
      ? fallbackOrParams
      : typeof paramsOrFallback === 'object'
        ? paramsOrFallback
        : undefined;
    const template = this.dictionary()[key] ?? TRANSLATIONS.es[key] ?? fallback ?? key;
    if (template === key && isDevMode()) {
      console.warn(`[i18n] Missing key: ${key}`);
    }
    return this.interpolate(template, interpolation);
  }

  private readLanguage(): UiLanguage {
    try {
      return this.normalize(sessionStorage.getItem(STORAGE_KEY));
    } catch {
      return 'es';
    }
  }

  private normalize(value: string | null | undefined): UiLanguage {
    return value === 'en' || value === 'pt' || value === 'fr' ? value : 'es';
  }

  private interpolate(template: string, params: Record<string, unknown> | undefined): string {
    if (!params) {
      return template;
    }
    return template.replace(/\{\{\s*([\w.-]+)\s*\}\}/g, (_match, key: string) => String(params[key] ?? ''));
  }
}
